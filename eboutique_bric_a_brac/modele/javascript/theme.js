/**
 * Used to create a theme to apply on the web site
 * 
 * @type type
 */
class theme{
    
    /**
     * 
     * @param {type} backgroundColor
     * @param {type} textColor
     * @param {type} caseColor
     * @returns {theme}
     */
    constructor(backgroundColor, textColor, caseColor){
        this.backgroundColor = backgroundColor;
        this.textColor = textColor;
        this.caseColor = caseColor;
    }
}

